# Program to work with two numbers
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Display the numbers
print(f"First number: {num1}")
print(f"Second number: {num2}")

# Some basic operations with the numbers
sum_result = num1 + num2
difference = num1 - num2

print(f"\nSum of the numbers: {sum_result}")
print(f"Difference between the numbers: {difference}")